        <h1 class="display-3">Mass Rapid Transit</h1>
        <p class="lead">PT Mass Rapid Transit Jakarta (PT MRT Jakarta) berdiri pada tanggal 17 Juni 2008, berbentuk badan hukum Perseroan Terbatas dengan mayoritas saham dimiliki oleh Pemerintah Provinsi DKI Jakarta (struktur kepemilikan: Pemprov DKI Jakarta 99.98%, PD Pasar Jaya 0.02%)​. PT MRT Jakarta memiliki ruang lingkup kegiatan di antaranya untuk pengusahaan dan pembangunan prasarana dan sarana MRT, pengoperasian dan perawatan (operation and maintenance/O&M) prasarana dan sarana MRT, serta pengembangan dan pengelolaan properti/bisnis di stasiun dan kawasan sekitarnya, serta Depo dan kawasan sekitarnya.</p>
        <p class="lead">Rencana pembangunan MRT di Jakarta sesungguhnya sudah dirintis sejak tahun 1985. Namun, saat itu proyek MRT belum dinyatakan sebagai proyek nasional. Pada tahun 2005, Presiden Republik Indonesia menegaskan bahwa proyek MRT Jakarta merupakan proyek nasional. Berangkat dari kejelasan tersebut, maka Pemerintah Pusat dan Pemerintah Provinsi DKI Jakarta mulai bergerak dan saling berbagi tanggung jawab. Pencarian dana disambut oleh Pemerintah Jepang yang bersedia memberikan pinjaman.</p>
        <p class="lead">Proyek MRT Jakarta dimulai dengan pembangunan jalur MRT Fase I sepanjang ± 16 kilometer dari Terminal Lebak Bulus hingga Bundaran Hotel Indonesia yang memiliki 13 stasiun berikut 1 Depo. Untuk meminimalisir dampak pembangunan fisik Fase I, selain menggandeng konsultan manajemen lalu lintas, PT MRT Jakarta juga memiliki Analisis Mengenai Dampak Lingkungan (Amdal). Pengoperasian Fase I akan dimulai pada tahun 2019.</p>
        <p class="lead">Pembangunan jalur MRT Fase I akan menjadi awal sejarah pengembangan jaringan terpadu dari sistem MRT yang merupakan bagian dari sistem transportasi massal DKI Jakarta pada masa mendatang. Pengembangan selanjutnya meneruskan jalur Sudirman menuju Ancol (disebut jalur Utara-Selatan) serta pengembangan jalur Timur-Barat.</p>

        <ul class="lead">
                <li>Dalam tahap Engineering Service, PT MRT Jakarta bertanggung jawab terhadap proses prakualifikasi dan pelelangan kontraktor.</li>
                <li>Dalam tahap Konstruksi, PT MRT Jakarta sebagai atribusi dari Pemerintah Provinsi DKI Jakarta menandatangani kontrak dengan kontraktor pelaksana konstruksi, dan konsultan yang membantu proses pelelangan kontraktor, serta konsultan manajemen dan operasional.</li>
                <li>Dalam tahap operasi dan pemeliharaan, PT MRT Jakarta bertanggung jawab terhadap pengoperasian dan perawatan, termasuk memastikan agar tercapainya jumlah penumpang yang cukup untuk memberikan pendapatan yang layak bagi perusahaan.​</li>
        </ul>

        <p class="lead">Pelaksanaan pembangunan MRT melibatkan beberapa instansi, baik pada tingkatan Pemerintah Pusat, Pemerintah Provinsi DKI Jakarta, dan PT MRT Jakarta sendiri. Oleh karena itu, dokumen anggaran yang diperlukan juga melibatkan lembaga-lembaga tersebut dengan nama program dan kegiatan berbeda namun dengan satu keluaran yang sama, pembangunan MRT Jakarta.</p>
        
